my_string = input('') # запрос ввода с клавиатуры значения 
print(len(my_string)) # вывод количества символов
print(my_string.upper()) # перевод текста в верхний регистр
print(my_string.lower()) # перевод текста в нижний регистр
print(my_string.replace(' ', '')) # замена символов (вместо пробела - ничего)
print(my_string[0]) # вывод первого символа
print(my_string[-1]) # вывод последнего символа
